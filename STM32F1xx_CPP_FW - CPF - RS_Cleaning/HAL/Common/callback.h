////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/// @file Callback.hpp
///
/// Interface for all callback functions in the Channel Platform Firmware. The application can use this interface to
/// create callback functions.
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

#ifndef CALLBACK_H
#define CALLBACK_H


namespace HAL
{

class Callback
{
    public:

        //**************************************************************************************************************
        // Public methods
        //**************************************************************************************************************

        ////////////////////////////////////////////////////////////////////////////////////////////////////////////////
        /// FUNCTION NAME: Callback::Callback
        ///
        /// Constructor
        ///
        /// @param  none
        /// @return none
        ////////////////////////////////////////////////////////////////////////////////////////////////////////////////
        Callback(){};

        ////////////////////////////////////////////////////////////////////////////////////////////////////////////////
        /// FUNCTION NAME: Callback::~Callback
        ///
        /// Virtual Destructor
        ///
        /// @param  none
        /// @return none
        ////////////////////////////////////////////////////////////////////////////////////////////////////////////////
        virtual ~Callback(){};

        ////////////////////////////////////////////////////////////////////////////////////////////////////////////////
        /// FUNCTION NAME: Callback::CallbackFunction
        ///
        /// Interface for a callback function
        ///
        /// @par Full Description
        /// Pure virtual function that will be overridden by the subclasses
        ///
        /// @param  none
        /// @return none
        ////////////////////////////////////////////////////////////////////////////////////////////////////////////////
        virtual void CallbackFunction() = 0;
};

} // end namespace CpfBsp

#endif // CALLBACK_H

